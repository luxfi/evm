// Process-level proof that wiring the Block-STM executor into StateProcessor
// (behind parallel.Enabled) is consensus-safe end to end. Unlike the executor's
// own determinism corpus (which compares roots in isolation), these tests drive
// real blocks through the FULL validation pipeline — InsertChain → Process →
// BlockValidator.ValidateState, which checks gas, bloom, the receipt-trie root
// AND the state root against each block's header. So a block accepted with the
// flag ON is proof the parallel path produced byte-identical state AND receipts.
//
// The blocks are generated by the sequential chain maker, so every header.Root is
// the sequential state root; inserting them with the flag ON forces ExecuteVerified
// to reproduce that exact root (and receipts) or the block is rejected.

